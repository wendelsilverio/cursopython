"""
Operadores aritméticos
+, -, *, /, //, **, %, ()
"""
print('Adição:', 10 + 10)
print('Concatenação:', '5' + '5')

print('Subtração:', 10 - 5)

print('Multiplicação:', 10 * 10)
print('Repetição:', 10 * '10')

print('Divisão:', 10 / 3)
print('Divisão inteira:', 10.5 // 3)
print('Divisão inteira:', 10 // 3)

print('Potência:', 2 ** 2)

print('Resto da divisão:', 10 % 3)

print('Precedência:', (5 + 2) * 10)